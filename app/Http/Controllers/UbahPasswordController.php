     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'password_lama' => ['required', new MatchOldPassword],
            'password_baru' => ['required'],
            'konfirmasi_password_baru' => ['same:password_baru'],
        ]);
   
        User::find(auth()->user()->id)->update(['password'=> Hash::make($request->password_baru)]);

        alert()->success('Berhasil', 'Ubah Password');
        return redirect('/akun/ubah-password')->with('message', 'Password Berhasil Di Update');
    }

